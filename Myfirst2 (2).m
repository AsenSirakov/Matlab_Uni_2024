% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
    a1 = str2double(get (handles.edit1, 'String'));
    a2 = str2double(get(handles.edit2, 'String'));
    h = (a2 - a1) / 500;
    x = a1:h:a2;
   y1 = exp(-x) .* sin(11 * x);
 axes(handles.axes2d);
 plot(x,y1);
    title('Графика на f(x) = e^{-x} \cdot sin(11x)');
    xlabel('x');
    ylabel('f(x)');
    x_min = str2double(get(handles.edit3, 'String'));
    x_max = str2double(get(handles.edit4, 'String'));
    y_min = str2double(get(handles.edit5, 'String'));
    y_max = str2double(get(handles.edit6, 'String'));
    x = linspace(x_min, x_max, 500);
    y = linspace(y_min, y_max, 500);
    [X, Y] = meshgrid(x, y);
    Z_3d = (exp(X.^2 + Y.^2) .* sin(X.*Y)) ./ (X + Y);
    axes(handles.axes3d);
    surf(X, Y, Z_3d);
    title('3D Графика на f(x, y) = e^{x^2+y^2} * sin(xy) / (x + y)');
    xlabel('x');
    ylabel('y');
    zlabel('f(x, y)');



